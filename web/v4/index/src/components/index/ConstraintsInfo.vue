      <h2 id="%E5%85%B7%E4%BD%93%E6%B5%81%E7%A8%8B">具体流程</h2>
      <ol>
        <li>
          <p>合作。当乙方确定接受甲方的约稿时，一个点对点的合作就建立了。</p>
        </li>
        <li>
          <p>合作期。乙方绘制到以下阶段时向甲方反馈进度，<font color="red"> 任何阶段 </font>甲方均可以向乙方提出修改意见：</p>
          <ul>
            <li>
              <p><strong>草稿期。</strong> 当甲方<font color="deepskyblue"> <strong>确定采用</strong> </font>乙方反馈过来的草稿时，画师准备进入线稿阶段，此时乙方应当支付<font
                  color="red"> 约定好价格的30%~50% </font>作为押金，支付范围区间由甲方决定。<font
                  color="red"><strong>完成支付之前不会进入下一阶段。</strong></font></p>
            </li>
            <li>
              <p><strong>线稿期。</strong> 当甲方 <font color="deepskyblue"> <font
                  color="deepskyblue"><i>确定采用</i></font></font>
                乙方反馈过来的草稿时，画师准备进入勾线阶段。</p>
            </li>
            <li>
              <p><strong>色稿期。</strong> 当甲方 <font color="deepskyblue"><i>确定采用</i></font> 乙方反馈过来的线稿时，画师准备进入上色阶段。</p>
            </li>
            <li>
              <p><strong>完稿期。</strong> 当甲方 <font color="deepskyblue"><i>确定采用</i></font>
                乙方反馈的完稿例图时，画师准备进入交付阶段。此时乙方应当支付<font color="red"> 约定好价格的50%~70% </font>清算尾款，尾款与押金的总和为定金的价格，<font
                    color="red"><strong>完成支付之前不会进入下一阶段。</strong></font></p>
            </li>
            <li>
              <p><strong>交付期。</strong>
                当乙方款项清算完毕时，进入交付期，甲方仍可提出修改意见，修改意见是否合理由乙方决定。确定不再有需要修改的地方之后，乙方会将甲方需要的所有源文件打包并加密附上解压密码发送至其<u>邮箱</u>。至此一个合作的周期结束。
              </p>
            </li>
          </ul>
        </li>
        <li>
          <p>终止合作。在任何阶段甲乙双方均可以提出终止合作,对于已付押金的，根据以下情况返还押金：</p>
          <ul>
            <li>
              <p>由乙方提出，且刚到草稿阶段或线稿阶段中期，全额返还押金，交付稿件源文件。</p>
            </li>
            <li>
              <p>由乙方提出，已完成线稿，返还押金的30%，同时交付稿件源文件。</p>
            </li>
            <li>
              <p>由乙方提出，已完成色稿中期，不返还押金，交付稿件源文件。</p>
            </li>
            <li>
              <p>由甲方提出，已完成草稿或到线稿中期，返还押金的30%，交付稿件源文件。</p>
            </li>
            <li>
              <p>由甲方提出，已完成线稿，不返还押金，交付稿件源文件。</p>
            </li>
          </ul>
        </li>
      </ol>
