</code></pre>
      <ol>
        <li>
          <p>在向画师提出约稿请求时默认 <strong><b>您已经从各个平台了解到该画师的作品风格</b></strong>，您是在知晓其风格偏好的情况下提出的约稿请求，如果交稿时您额外提出整体风格修改的要求，画师有权驳回，因此如果您想要画师画不同的风格时，您应该在初期沟通的阶段（画师开始绘制之前）就告知画师具体想要的风格，风格改动必须提供例图。
          </p>
        </li>
        <li>
          <p>在向画师提出约稿请求前，您应当确保您根据第一条约束已经了解到画师的具体水平，避免期望过高。</p>
        </li>
        <li>
          <p>您应当明确告知您的一般在线时间，确保在这个时间内画师向您提供反馈时您可以及时回复。</p>
        </li>
        <li>
          <p>您应当明确告知画成什么样的风格，对于人物角色，如果非三视图，您应当明确告知角色的动态如何、正在干什么、周围物件是什么、何种神情、何种氛围、何种镜头等关键要素。</p>
        </li>
        <li>
          <p>对于同人绘制，如果您需要背景，那么您应该明确告知什么样的背景，同时您应该提供人物角色的基础设定、世界观来作为氛围参考。</p>
        </li>
        <li>
          <p>对于同人绘制，您应该附上原人设例图、三视图等必要参考，对于没有三视图的情况，您应该尽可能详细的文字描述并附相应的参考图。</p>
        </li>
        <li>
          <p>
            对于同人绘制，如果您提供的例图为草稿等半成品阶段，如果例图并未完全上色，您应当告知画师真正的结构/颜色是什么样的，默认情况下，画师会选择自己认为合适的例图颜色作为相应块的底色，所以这与您的真实想法可能会有出入。</p>
        </li>
        <li>
          <p>对于非画师原创的作品，您应该明确告知可发布权限，您可以：</p>
